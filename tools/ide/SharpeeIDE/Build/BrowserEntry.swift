// BrowserEntry.swift
// Knows about the browser entry point a story needs to build/play in the browser
// (`<story>/src/browser-entry.ts`): where it lives, whether it exists, and how to
// generate a starter one on the author's consent.
// Public interface: BrowserEntry.path/exists/create(repoRoot:story:), template(story:).
// Owner context: tools/ide — Build.

import Foundation

enum BrowserEntryError: LocalizedError {
    case storyNotFound(String)
    var errorDescription: String? {
        switch self {
        case .storyNotFound(let story):
            return "Couldn't find a story directory for '\(story)' under stories/ or tutorials/."
        }
    }
}

enum BrowserEntry {

    /// The browser entry path (`<storyDir>/src/browser-entry.ts`), or nil if the story
    /// directory isn't found under stories/ or tutorials/.
    static func path(repoRoot: URL, story: String) -> URL? {
        guard let dir = storyDirectory(repoRoot: repoRoot, story: story) else { return nil }
        return dir.appendingPathComponent("src").appendingPathComponent("browser-entry.ts")
    }

    /// True iff the story has a browser entry on disk.
    static func exists(repoRoot: URL, story: String) -> Bool {
        guard let path = path(repoRoot: repoRoot, story: story) else { return false }
        return FileManager.default.fileExists(atPath: path.path)
    }

    /// Writes a generated starter browser entry for `story`, creating `src/` if needed.
    /// Returns the file URL. Throws if the story directory can't be found.
    @discardableResult
    static func create(repoRoot: URL, story: String) throws -> URL {
        guard let path = path(repoRoot: repoRoot, story: story) else {
            throw BrowserEntryError.storyNotFound(story)
        }
        try FileManager.default.createDirectory(at: path.deletingLastPathComponent(),
                                                withIntermediateDirectories: true)
        try template(story: story).write(to: path, atomically: true, encoding: .utf8)
        return path
    }

    private static func storyDirectory(repoRoot: URL, story: String) -> URL? {
        let fm = FileManager.default
        for base in ["stories", "tutorials"] {
            let dir = repoRoot.appendingPathComponent(base).appendingPathComponent(story)
            var isDir: ObjCBool = false
            if fm.fileExists(atPath: dir.path, isDirectory: &isDir), isDir.boolValue { return dir }
        }
        return nil
    }

    /// The generated starter entry. Reads metadata from `story.config` so it compiles
    /// whether or not the story exports a `config` const.
    static func template(story: String) -> String {
        """
        /**
         * Browser entry point for \(story) — generated by Sharpee IDE.
         *
         * A starting point that wires this story into the browser client. Add any
         * story-specific channel/audio renderers before `client.start()` — see
         * stories/dungeo/src/browser-entry.ts for a fully-featured example.
         */

        import { GameEngine } from '@sharpee/engine';
        import { WorldModel, EntityType } from '@sharpee/world-model';
        import { Parser } from '@sharpee/parser-en-us';
        import { LanguageProvider } from '@sharpee/lang-en-us';
        import { PerceptionService } from '@sharpee/stdlib';
        import { BrowserClient, ThemeManager } from '@sharpee/platform-browser';
        import { story } from './index';
        import { STORY_VERSION, ENGINE_VERSION, BUILD_DATE } from './version';

        const THEME_STORAGE_KEY = '\(story)-theme';
        ThemeManager.applyEarlyTheme(THEME_STORAGE_KEY);

        const author = story.config.author;
        const client = new BrowserClient({
          storagePrefix: '\(story)-',
          defaultTheme: 'modern-dark',
          themes: [
            { id: 'modern-dark', name: 'Modern Dark' },
            { id: 'paper', name: 'Paper' },
          ],
          storyInfo: {
            title: story.config.title,
            description: story.config.description || '',
            authors: Array.isArray(author) ? author.join(', ') : author,
            version: STORY_VERSION,
            engineVersion: ENGINE_VERSION,
            buildDate: BUILD_DATE,
          },
        });

        async function start(): Promise<void> {
          client.initialize({
            statusLocation: document.getElementById('location-name'),
            statusScore: document.getElementById('score-turns'),
            textContent: document.getElementById('text-content'),
            mainWindow: document.getElementById('main-window'),
            commandInput: document.getElementById('command-input') as HTMLInputElement,
            saveDialog: document.getElementById('save-dialog') as HTMLDialogElement,
            restoreDialog: document.getElementById('restore-dialog') as HTMLDialogElement,
            startupDialog: document.getElementById('startup-dialog') as HTMLDialogElement,
            saveNameInput: document.getElementById('save-name-input') as HTMLInputElement,
            saveSlotsListEl: document.getElementById('save-slots-list'),
            restoreSlotsListEl: document.getElementById('restore-slots-list'),
            noSavesMessage: document.getElementById('no-saves-message'),
            startupSaveInfo: document.getElementById('startup-save-info'),
            menuBar: document.getElementById('menu-bar'),
          });

          const world = new WorldModel();
          const player = world.createEntity('player', EntityType.ACTOR);
          world.setPlayer(player.id);

          const language = new LanguageProvider();
          const parser = new Parser(language);
          if (story.extendParser) story.extendParser(parser);
          if (story.extendLanguage) story.extendLanguage(language);

          const perceptionService = new PerceptionService();
          const engine = new GameEngine({ world, player, parser, language, perceptionService });

          client.connectEngine(engine, world);
          engine.setStory(story);
          engine.registerSaveRestoreHooks(client.getSaveRestoreHooks());

          await client.start();
        }

        if (document.readyState === 'loading') {
          document.addEventListener('DOMContentLoaded', start);
        } else {
          start();
        }

        """
    }
}
